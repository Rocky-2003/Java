public class Index {
  public static void main(String[] args) {
    int a = 2;
    int b = 3;

    // == Equal To
    System.out.println(a == b);
    // != Not Equal To
    System.out.println(a != b);
    // < less than
    System.out.println(a < b);
    // > greater than
    System.out.println(a > b);
    // <= less than Equal to
    System.out.println(a <= b);
    // >= greater than equal to
    System.out.println(a >= b);
  }
}
