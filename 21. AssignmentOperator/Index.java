public class Index {
  public static void main(String[] args) {
    int a = 4;

    // += plusEqualTo
    a += 1;
    System.out.println(a);
    // -= minusEqualTo
    a -= 1;
    System.out.println(a);
    // *= multiplyEqualTo
    a *= 2;
    System.out.println(a);
    // /= divideEqualTo
    a /= 2;
    System.out.println(a);
    // %= moduloEqualTo
    a %= 3;
    System.out.println(a);
  }
}
