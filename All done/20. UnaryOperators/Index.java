public class Index {
  public static void main(String[] args) {
    int a = 4;
    int b = 4;
    int c = 4;
    int d = 4;
    boolean l = false;

    // Unary Minus
    System.out.println(-a);  
    // Unary Plus
    System.out.println(+a);
    // Post-Increment
    System.out.println(a++);
    // Pre-Increment
    System.out.println(++b);
    // Post-Decrement
    System.out.println(c--);
    // Pre-Decrement
    System.out.println(--d);
    // Logical Not Operator
    System.out.println(!l);

  }
  
}
