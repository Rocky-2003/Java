public class Index {
  public static void main(String[] args) {

    int a = 2;
    int b = 4;

    // Multiplication
    System.out.println(a * b);
    // Division
    System.out.println(b / a);
    // Modulo give the reminder of the divion
    System.out.println(b % a);
    // Addition
    System.out.println(a + b);
    // Substration
    System.out.println(a - b);

  }
}
