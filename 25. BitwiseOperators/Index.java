public class Index {
  public static void main(String[] args) {
    int a = 4; // 0100
    int b = 7; // 0111
    int d = 5;

    // Bitwise AND `&`
    System.out.println(a & b);
    // Bitwise OR `|`
    System.out.println(a | b);
    // Bitwise XOR `^`
    System.out.println(a ^ b);
    // Bitwise Not `~`
    System.out.println(~d);

  }
}
